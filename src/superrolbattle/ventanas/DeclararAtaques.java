    private ArrayList<Token> listadeEnemigosPosibles() {
        ArrayList<Token> enemigos = new ArrayList<Token>();
        enemigos.add(Token.nadie());

        for (Token enemigo : Recursos.soldados) {
            boolean mismoLado = enemigo.isLadoIzquierdo() == soldado.isLadoIzquierdo();

            if (!enemigo.equals(soldado) && (principal.dosLados || !mismoLado)) {
                enemigos.add(enemigo);
            }

        }
        return enemigos;
    }

    private void controlDistancia() {
        Arma arm = (Arma) jComboBoxArmas.getSelectedItem();
        if (arm.getClase() == Arma.CLASE_ARCO_CORTO || arm.getClase() == Arma.CLASE_ARCO_LARGO) {
            jPanel_distancia.setVisible(true);
        } else {
            jPanel_distancia.setVisible(false);
        }
        this.repaint();
    }

    private int bonoSegunDistancia() {
        int dist = (Integer) jSpinner_distancia.getValue();
        int bono = 0;
        Arma arm = (Arma) jComboBoxArmas.getSelectedItem();
        if (arm.getClase() == Arma.CLASE_ARCO_CORTO) {
            if (dist <= 3) {
                bono = 10;
            } else if (dist >= 30 && dist <= 40) {
                bono = -40;
            } else if (dist >= 40 && dist <= 50) {
                bono = -70;
            } else if (dist >= 51) {
                bono = -100;
            }
        } else if (arm.getClase() == Arma.CLASE_ARCO_LARGO) {
            if (dist <= 3) {
                bono = 20;
            } else if (dist >= 30 && dist <= 44) {
                bono = -30;
            } else if (dist >= 45 && dist <= 60) {
                bono = -45;
            } else if (dist >= 61 && dist <= 119) {
                bono = -60;
            } else if (dist >= 120) {
                bono = -60;
            }

        }
        return bono;
    }


