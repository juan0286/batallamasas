    private void modificarBO() {
        DefaultListModel jdlm = (DefaultListModel) jList_combatientes.getModel();
        JPanelFormToken_Accion jpta = (JPanelFormToken_Accion) jdlm.getElementAt(jList_combatientes.getSelectedIndex());
        
        int cant = (Integer) jSpinner_cant_cambios.getValue();
        int bon = jpta.getAccion().getCombate().getBo_Disponible();        
        int bono_ene = (jList_blancos.getSelectedIndices().length >1)? (jList_blancos.getSelectedIndices().length-1)*-50 :0;
        bon = (cant * -30) + bon +  bono_ene;
        jTextField_boDisponible.setText("" + bon);
    }


