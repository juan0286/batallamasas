    private void guardarArma() {
        int val = (Integer) jSpinner_cant_cambios.getValue();
        jSpinner_cant_cambios.setValue(val + 1);
        modificarBO();
    }

    private void equiparArma() {
        int val = (Integer) jSpinner_cant_cambios.getValue();
        jSpinner_cant_cambios.setValue(val + 1);
        modificarBO();
    }

    private void iniciarAtaques() {
        ArrayList<JPanelFormToken_Accion> enemigos = new ArrayList<>();
        DefaultListModel jdlm = (DefaultListModel) jList_blancos.getModel();
        
        for (Integer i : jList_blancos.getSelectedIndices()) {
            enemigos.add((JPanelFormToken_Accion) jdlm.get(i));
        }
        jdlm = (DefaultListModel) jList_combatientes.getModel();       
        
        JPanelFormToken_Accion jpa = (JPanelFormToken_Accion) jdlm.get(jList_combatientes.getSelectedIndex());
        jdlm = (DefaultListModel) jList_armas.getModel();       
        Arma arma = (Arma) jdlm.get(jList_armas.getSelectedIndex());
        
        ResolverAtaques ra = new ResolverAtaques(principal, true, jpa, enemigos, Integer.parseInt(jTextField_boDisponible.getText()),arma);
        ra.setVisible(true);
    }

    private void activarAtaqueButton() {
        boolean act = false;
        act = (!jList_blancos.isSelectionEmpty() && !jList_armas.isSelectionEmpty());
        jButton_atacar.setEnabled(act);
    }

