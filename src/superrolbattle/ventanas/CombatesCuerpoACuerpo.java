    private void seleccionarPersonaje() {

        jSpinner_cant_cambios.setValue(0);
        DefaultListModel jdlm = (DefaultListModel) jList_combatientes.getModel();
        JPanelFormToken_Accion jpta = (JPanelFormToken_Accion) jdlm.getElementAt(jList_combatientes.getSelectedIndex());
        
        //jComboBox_enemigos.setModel(new DefaultComboBoxModel());
        jdlm = (DefaultListModel) jList_blancos.getModel();
        jdlm.removeAllElements();
        for (Object enemigo : principal.getTodosLosJPanelEnemigos(jpta.getToken()).toArray()) {
            enemigo = (JPanelFormToken_Accion) enemigo;
            jdlm.addElement(enemigo);
        }
        jList_blancos.setModel(jdlm);

        Accion a = jpta.getAccion();
        DatosCombate dc = a.getCombate();
        jTextField_boDisponible.setText(dc.getBo_Disponible() + "");
        jSpinner_cant_cambios.setValue(dc.getCantidadCambios());

        jdlm = (DefaultListModel) jList_armas.getModel();
        jdlm.removeAllElements();
        for (Extremidad ext : jpta.getToken().getExtremidades()) {
            Arma arma = ext.getArmaEquipada();
            if (arma != null) {                
                jdlm.addElement(arma);               
            }
        }
        jList_armas.setModel(jdlm);
    }

